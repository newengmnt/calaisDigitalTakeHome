def main():
    parser = argparse.ArgumentParser(description="Run the full sell-call backtest pipeline.")
    parser.add_argument("--input", default="./data/full_data_clean.parquet",
                         help="Raw cleaned parquet, used only if --candidates-file is not given.")
    parser.add_argument("--candidates-file", default=None,
                         help="Pre-built candidates.parquet -- if given, preprocessing is skipped.")
    parser.add_argument("--runs-dir", default="./outputs/runs")
    parser.add_argument("--run-name", default=None)
    parser.add_argument("--initial-capital", type=float, default=1_000_000.0)

    parser.add_argument("--skip-tests", action="store_true", help="Skip running the unit test suite first.")
    parser.add_argument("--output-level", choices=OUTPUT_LEVELS, default="standard",
                         help="minimal: execution_summary.json only. standard: full CSV/JSON/plot "
                              "report. full: standard + an analysis.ipynb notebook.")

    parser.add_argument("--hedge-threshold", type=float, default=trade_engine.HEDGE_THRESHOLD)
    parser.add_argument("--max-open-positions", type=int, default=trade_engine.MAX_OPEN_POSITIONS)
    parser.add_argument("--option-lot-size", type=float, default=trade_engine.OPTION_LOT_SIZE)
    parser.add_argument("--min-score", type=float, default=trade_engine.MIN_SCORE)
    parser.add_argument("--underlying-lot-size", type=float,
                         default=trade_engine.UNDERLYING_LOT_SIZE)

    parser.add_argument("--w-theta", type=float, default=None)
    parser.add_argument("--w-vega", type=float, default=None)
    parser.add_argument("--w-gamma", type=float, default=None)
    parser.add_argument("--w-spread", type=float, default=None)
    args = parser.parse_args()

    t0 = time.time()

    if not args.skip_tests:
        print("Running unit tests...")
        if not run_unit_tests():
            print("Unit tests FAILED -- aborting backtest run.")
            sys.exit(1)
        print("Unit tests passed.\n")
    else:
        print("Skipping unit tests (--skip-tests).\n")

    run_dir = make_run_dir(args.runs_dir, args.run_name)
    print(f"Run directory: {run_dir}")

    if args.candidates_file:
        candidates_path = args.candidates_file
        candidates_meta_path = sibling_meta_path(candidates_path)
        print(f"Using existing candidates file: {candidates_path} (preprocessing skipped)")
    else:
        candidates_path = os.path.join(run_dir, "candidates.parquet")
        candidates_meta_path = os.path.join(run_dir, "candidates_meta.json")
        print("Running preprocessing...")
        preprocessing.run(
            source=args.input, output=candidates_path, meta_output=candidates_meta_path,
            weights=preprocessing.ScoreWeights(
                w_theta=args.w_theta, w_vega=args.w_vega,
                w_gamma=args.w_gamma, w_spread=args.w_spread,
            ),
        )

    cand = pd.read_parquet(candidates_path)

    print("Running trade engine...")
    trades_df, hedge_trades_df = trade_engine.generate_trades(
        cand,
        hedge_threshold=args.hedge_threshold,
        max_open_positions=args.max_open_positions,
        option_lot_size=args.option_lot_size,
        min_score=args.min_score,
        underlying_lot_size=args.underlying_lot_size,
    )
    print(f"  {len(trades_df)} option trades, {len(hedge_trades_df)} hedge trades")

    print("Running minute snapshot...")
    snapshot_df = minute_snapshot.generate_minute_snapshot(
        cand, trades_df, hedge_trades_df, args.initial_capital)

    run_config = {
        "generated_at_utc": datetime.now(timezone.utc).isoformat(),
        "candidates_file": candidates_path,
        "preprocessing_skipped": bool(args.candidates_file),
        "initial_capital": args.initial_capital,
        "hedge_threshold": args.hedge_threshold,
        "max_open_positions": args.max_open_positions,
        "option_lot_size": args.option_lot_size,
        "min_score": args.min_score,
        "underlying_lot_size": args.underlying_lot_size,
        "output_level": args.output_level,
        "unit_tests_run": not args.skip_tests,
    }

    if args.output_level == "minimal":
        # Only file written at this level: execution_summary.json. trades/
        # hedge_trades/minute_snapshot stay in memory and are never persisted.
        print("Building minimal execution_summary.json...")
        summary = build_minimal_summary(
            snapshot_df, trades_df, hedge_trades_df, cand,
            candidates_meta_path, args.initial_capital)
        summary["run_config"] = run_config
        with open(os.path.join(run_dir, "execution_summary.json"), "w") as f:
            json.dump(summary, f, indent=2)
    else:
        trades_path = os.path.join(run_dir, "trades.csv")
        hedge_trades_path = os.path.join(run_dir, "hedge_trades.csv")
        snapshot_path = os.path.join(run_dir, "minute_snapshot.csv")
        trades_df.to_csv(trades_path, index=False)
        hedge_trades_df.to_csv(hedge_trades_path, index=False)
        snapshot_df.to_csv(snapshot_path, index=False)

        with open(os.path.join(run_dir, "run_config.json"), "w") as f:
            json.dump(run_config, f, indent=2)

        print("Running full report (CSVs, risk summary, plots)...")
        report.run(
            snapshot_path=snapshot_path, trades_path=trades_path,
            hedge_trades_path=hedge_trades_path, candidates_path=candidates_path,
            candidates_meta_path=candidates_meta_path, output_dir=run_dir,
            initial_capital=args.initial_capital,
        )
        if args.output_level == "full":
            print("Generating analysis notebook...")
            generate_notebook(run_dir, os.path.join(run_dir, "analysis.ipynb"))

    elapsed = time.time() - t0
    print(f"\nDone in {elapsed:.1f}s. Outputs in {run_dir}/")


if __name__ == "__main__":
    main()
