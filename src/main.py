"""Top-level orchestrator: [optional] unit tests -> [optional] preprocessing
-> trade_engine -> minute_snapshot -> [optional] report -> [optional] notebook.

Each invocation gets its own run directory (outputs/runs/run_<timestamp> by
default, or --run-name / an explicit --run-dir) so repeated runs with
different parameters don't clobber each other.

Run:
    python src/main.py
    python src/main.py --candidates-file data/candidates.parquet --skip-tests
    python src/main.py --hedge-threshold 0.05 --max-open-positions 3 --output-level full
    python src/main.py --output-level minimal
"""
import argparse
import json
import os
import sys
import time
from datetime import datetime, timezone

import pandas as pd

import preprocessing
import trade_engine
import minute_snapshot
import report

OUTPUT_LEVELS = ["minimal", "standard", "full"]


def run_unit_tests() -> bool:
    import pytest
    tests_dir = os.path.join(os.path.dirname(__file__), "..", "tests")
    exit_code = pytest.main(["-q", tests_dir])
    return exit_code == 0


def make_run_dir(runs_base: str, run_name: str = None) -> str:
    name = run_name or f"run_{datetime.now(timezone.utc).strftime('%Y%m%d_%H%M%S')}"
    run_dir = os.path.join(runs_base, name)
    os.makedirs(run_dir, exist_ok=True)
    return run_dir


def sibling_meta_path(candidates_path: str) -> str:
    """preprocessing.py writes candidates_meta.json next to candidates.parquet
    under the same stem; look for it there when reusing an existing file."""
    stem = os.path.splitext(candidates_path)[0]
    return f"{stem}_meta.json"


def build_minimal_summary(snapshot_df: pd.DataFrame, trades_df: pd.DataFrame,
                           hedge_trades_df: pd.DataFrame, cand: pd.DataFrame,
                           candidates_meta_path: str, initial_capital: float) -> dict:
    """Cheap path for --output-level minimal: works entirely on in-memory
    DataFrames (never writes trades.csv/hedge_trades.csv/minute_snapshot.csv
    to the run directory) and reuses report.py's pure analysis functions --
    the only file this mode produces is execution_summary.json."""
    snapshot, trades, hedge_trades = report.enrich_inputs(snapshot_df, trades_df, hedge_trades_df, cand)
    daily = report.build_daily_pnl(snapshot, trades, initial_capital)
    warnings = report.collect_data_quality_warnings(trades, hedge_trades, candidates_meta_path)
    risk_summary = report.compute_risk_summary(daily, trades, hedge_trades, snapshot, initial_capital, warnings)
    execution_summary = report.build_execution_summary(trades, hedge_trades, initial_capital)
    return {
        "risk_summary": risk_summary,
        "execution_summary": execution_summary.iloc[0].to_dict() if len(execution_summary) else {},
    }


